test('resolvePorts: all three free → the configured defaults', () => {
  const ports = resolvePorts(DEFAULT_CONFIG, () => false);
  assert.deepStrictEqual(ports, { web: 7810, wirescope: 7811, wire: 7820 });
});

test('resolvePorts: collisions bump each port, no two collapse onto one', () => {
  // 7810 (web default) and 7820 (wire default) are held; web bumps to 7811, but
  // wirescope's default IS 7811, so it must bump again — and never onto web's.
  const busy = new Set([7810, 7820]);
  const ports = resolvePorts(DEFAULT_CONFIG, (p) => busy.has(p));
  assert.strictEqual(ports.web, 7811);
  assert.strictEqual(ports.wirescope, 7812); // 7811 taken by web this generation
  assert.strictEqual(ports.wire, 7821);
  // Distinctness invariant.
  assert.strictEqual(new Set([ports.web, ports.wirescope, ports.wire]).size, 3);
});

