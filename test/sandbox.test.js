test('generateCompose: dev build variant with a named work volume', () => {
  const yaml = generateCompose({ image: DEV_IMAGE, ports: PORTS, workDir: null, authEnvFile: null });
  assert.match(yaml, /^# GENERATED by Clodex/m);
  assert.match(yaml, /hostname: sandbox/);
  assert.match(yaml, /build:\n {6}context: \/repo\n {6}dockerfile: docker\/web\/Dockerfile/);
  // Three loopback publishes host<config> → container<fixed>.
  assert.match(yaml, /- "127\.0\.0\.1:7810:8080"/);
  assert.match(yaml, /- "127\.0\.0\.1:7811:7800"/);
  assert.match(yaml, /- "127\.0\.0\.1:7820:7900"/);
  // Named work volume + its declaration.
  assert.match(yaml, /- clodex-work:\/home\/clodex\/work/);
  assert.match(yaml, /^ {2}clodex-work:$/m);
  // Wirescope public URL tracks the host port.
  assert.match(yaml, /CLODEX_WIRESCOPE_PUBLIC_URL: "\$\{CLODEX_WIRESCOPE_PUBLIC_URL:-http:\/\/localhost:7811\}"/);
  // Compose interpolations survive untouched (not eaten by JS).
  assert.match(yaml, /CLODEX_WEB_TOKEN: "\$\{CLODEX_WEB_TOKEN:-\}"/);
  // Fixed lifecycle bits mirrored from docker/web/compose.yaml.
  assert.match(yaml, /init: true/);
  assert.match(yaml, /restart: always/);
  assert.match(yaml, /healthcheck:/);
  // No image: line in the dev/build variant, no env_file without an auth file.
  assert.doesNotMatch(yaml, /^ {4}image:/m);
  assert.doesNotMatch(yaml, /env_file:/);
});

